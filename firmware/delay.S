// *******************************************************
// 	10uS単位の遅延ルーチン と 7clk遅延ルーチン(retのみ)
// *******************************************************

//
//	delay_10us(uchar wait)
//
	.text

	.global	delay_10us
	.type	delay_10us, @function

	.global	delay_7clk
	.type	delay_7clk, @function

// *******************************************************
delay_10us:
	ldi		r25,40		// 40*3clk = 120clk = 10uS
loop1:
	subi	r25,1		//	1
	brne	loop1		//  2

	subi	r24,1		//	1
	brne	delay_10us	//  2
// *******************************************************
//
//	delay_7clk()	;	4+4 : call,ret
//
delay_7clk:
	ret

// *******************************************************

